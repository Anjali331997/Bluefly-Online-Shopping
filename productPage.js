function show_products(products_data) {
  document.querySelector("#product_div").innerText = "";
  products_data.map(function (e) {
    var div = document.createElement("div");

    var imagediv = document.createElement("div");
    imagediv.setAttribute("id", "imagediv");

    var anchor = document.createElement("a");
    anchor.setAttribute("href", "productDetails.html");
    anchor.setAttribute("id", "anchor_tag");
    var button = document.createElement("button");
    button.innerHTML = '<i class="fa-regular fa-heart"></i>';
    button.setAttribute("id", "button");
    button.addEventListener("click", function () {
      add_to_wishlist(e);
    });
    var product_img = document.createElement("img");
    product_img.setAttribute("onmouseover", onHover());
    product_img.setAttribute("onmouseout", offHover());

    function onHover() {
      product_img.setAttribute("src", e.image_link);
    }
    function offHover() {
      product_img.setAttribute("src", e.alternative_img_link);
    }

    imagediv.append(button, product_img);
    var product_name = document.createElement("p");
    product_name.innerText = e.product_name;
    product_name.setAttribute("id", "product_name");

    var pricing = document.createElement("div");
    pricing.setAttribute("id", "pricing");
    var striked_off_price_span = document.createElement("span");
    striked_off_price_span.innerText = e.striked_off_price;

    var striked_off_price_p = document.createElement("p");
    striked_off_price_p.innerText = "$";

    striked_off_price_p.setAttribute("id", "striked_off_price");

    var selling_price_span = document.createElement("span");
    selling_price_span.innerText = e.selling_price;

    var selling_price_p = document.createElement("p");
    selling_price_p.innerText = "$";

    var discount_span = document.createElement("span");
    discount_span.innerText = e.discount;

    var discount_p = document.createElement("p");
    discount_p.innerText = "SAVE ";

    discount_p.setAttribute("id", "discount");

    striked_off_price_p.append(striked_off_price_span);
    selling_price_p.append(selling_price_span);
    discount_p.append(discount_span);

    pricing.append(striked_off_price_p, selling_price_p, discount_p);
    div.append(imagediv, product_name, pricing);
    anchor.append(div);
    document.querySelector("#product_div").append(anchor);

    document.querySelector("span").innerText = products_data.length;
  });
}

document.querySelector("#sort").addEventListener("change", sortProducts);

function sortProducts() {
  var sortType = document.querySelector("#sort").value;
  console.log(sortType);
  console.log(sortType == "");
  if (sortType == "lth") {
    var lth = products_data.sort(function (a, b) {
      price1 = a.selling_price.replace(",", "");
      price2 = b.selling_price.replace(",", "");
      return price1 - price2;
    });
    show_products(lth);
  } else if (sortType == "htl") {
    var htl = products_data.sort(function (a, b) {
      price1 = a.selling_price.replace(",", "");
      price2 = b.selling_price.replace(",", "");
      return price2 - price1;
    });
    show_products(htl);
  } else if (sortType == "a_to_z") {
    var a_to_z = products_data.sort(function (a, b) {
      if (a.product_name > b.product_name) {
        return 1;
      } else if (a.product_name < b.product_name) {
        return -1;
      } else {
        return 0;
      }
    });
    show_products(a_to_z);
  } else if (sortType == "z_to_a") {
    var z_to_a = products_data.sort(function (a, b) {
      if (a.product_name < b.product_name) {
        return 1;
      } else if (a.product_name > b.product_name) {
        return -1;
      } else {
        return 0;
      }
    });
    show_products(z_to_a);
  } else if (sortType == "") {
    var relevance = products_data.filter(function (e) {
      return true;
    });
    show_products(relevance);
  }
}

